	AREA RESET,DATA,READONLY
	EXPORT __Vectors
__Vectors
	DCD 0x10001000
	DCD Reset_Handler
	ALIGN
	AREA mycode,CODE,READONLY
	ENTRY
	EXPORT Reset_Handler
Reset_Handler
	LDR R0,=NUM
	LDR R0,[R0]
	BL FACTORIAL
	LDR R1,=RESULT
	STR R0,[R1]
	
STOP B STOP

FACTORIAL
	PUSH {R1,LR}
	CMP R0,#0
	BEQ FACT_BASE
	MOV R1,R0
	SUB R0,R0,#1
	BL FACTORIAL
	MUL R0,R0,R1
	POP {R1,LR}
	BX LR
	
FACT_BASE
	MOV R0,#1
	POP {R1,LR}
	BX LR
	
	AREA mydata,DATA,READWRITE
NUM DCD 5
RESULT DCD 0
	END